     */
    public function index($perPage = 100, $page = 1)
    {
        /**
         * Note: that we are using eager loading to load the eventable relationship due to the limited time.
         * using joins and then fractal transformers would offer faster query time and the same results.
         */
        // get all events with their eventable models
        $events = Event::query()
            ->orderBy("created_at")
            ->with("eventable")
            ->paginate($perPage, ["*"], "page", $page);
        return response()->json($events);
    }

